        setSelectedTopicIds(defaultTopics);
        
        // Aynƒ± konu ID'sini alt konu olarak da kullan
        setSelectedSubTopicIds(defaultTopics);
        
        // G√∂r√ºnt√ºlenecek alt konu nesnesi olu≈ütur
